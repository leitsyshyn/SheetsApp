using System;
using System.Collections.Generic;
using System.Linq;
namespace SheetsApp
{
    public class Cell
    {
        public string Name { get; set; }
        public string Expression { get; set; }
        public string Value { get; set; }

        public Cell(string name, string expression = "", string value = "")
        {
            Name = name;
            Expression = expression;
            Value = value;
        }
    }
}
